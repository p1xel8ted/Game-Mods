  public static int GetPluralRule(string langCode)
  {
    if (langCode.Length > 2)
      langCode = langCode.Substring(0, 2);
    langCode = langCode.ToLower();
    foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
    {
      if (keyValuePair.Value.Code == langCode)
        return keyValuePair.Value.PluralRule;
    }
    return 0;
  }

  public static bool LanguageHasPluralType(string langCode, string pluralType)
  {
    if (pluralType == "Plural" || pluralType == "Zero" || pluralType == "One")
      return true;
    switch (GoogleLanguages.GetPluralRule(langCode))
    {
      case 3:
        return pluralType == "Two" || pluralType == "Few";
      case 4:
      case 5:
      case 6:
      case 7:
      case 8:
        return pluralType == "Few";
      case 9:
        return pluralType == "Two" || pluralType == "Few";
      case 10:
      case 11:
      case 15:
      case 16 /*0x10*/:
        return pluralType == "Two" || pluralType == "Few" || pluralType == "Many";
      case 12:
        return pluralType == "Few" || pluralType == "Many";
      case 13:
        return pluralType == "Two";
      default:
        return false;
    }
  }

  public static ePluralType GetPluralType(string langCode, int n)
  {
    if (n == 0)
      return ePluralType.Zero;
    if (n == 1)
      return ePluralType.One;
    switch (GoogleLanguages.GetPluralRule(langCode))
    {
      case 0:
        return ePluralType.Plural;
      case 1:
        return n != 1 ? ePluralType.Plural : ePluralType.One;
      case 2:
        return n > 1 ? ePluralType.Plural : ePluralType.One;
      case 3:
        switch (n)
        {
          case 1:
          case 11:
            return ePluralType.One;
          case 2:
          case 12:
            return ePluralType.Two;
          default:
            return !GoogleLanguages.inRange(n, 3, 10) && !GoogleLanguages.inRange(n, 13, 19) ? ePluralType.Plural : ePluralType.Few;
        }
      case 4:
        if (n == 1)
          return ePluralType.One;
        return !GoogleLanguages.inRange(n % 100, 1, 19) ? ePluralType.Plural : ePluralType.Few;
      case 5:
        if (n % 10 == 1 && n % 100 != 11)
          return ePluralType.One;
        return n % 10 < 2 || n % 100 >= 10 && n % 100 < 20 ? ePluralType.Plural : ePluralType.Few;
      case 6:
        if (n % 10 == 1 && n % 100 != 11)
          return ePluralType.One;
        return !GoogleLanguages.inRange(n % 10, 2, 4) || GoogleLanguages.inRange(n % 100, 12, 14) ? ePluralType.Plural : ePluralType.Few;
      case 7:
        if (n == 1)
          return ePluralType.One;
        return !GoogleLanguages.inRange(n, 2, 4) ? ePluralType.Plural : ePluralType.Few;
      case 8:
        if (n == 1)
          return ePluralType.One;
        return !GoogleLanguages.inRange(n % 10, 2, 4) || GoogleLanguages.inRange(n % 100, 12, 14) ? ePluralType.Plural : ePluralType.Few;
      case 9:
        if (n % 100 == 1)
          return ePluralType.One;
        if (n % 100 == 2)
          return ePluralType.Two;
        return !GoogleLanguages.inRange(n % 100, 3, 4) ? ePluralType.Plural : ePluralType.Few;
      case 10:
        if (n == 1)
          return ePluralType.One;
        if (n == 2)
          return ePluralType.Two;
        if (GoogleLanguages.inRange(n, 3, 6))
          return ePluralType.Few;
        return !GoogleLanguages.inRange(n, 7, 10) ? ePluralType.Plural : ePluralType.Many;
      case 11:
        switch (n)
        {
          case 0:
            return ePluralType.Zero;
          case 1:
            return ePluralType.One;
          case 2:
            return ePluralType.Two;
          default:
            if (GoogleLanguages.inRange(n % 100, 3, 10))
              return ePluralType.Few;
            return n % 100 < 11 ? ePluralType.Plural : ePluralType.Many;
        }
      case 12:
        if (n == 1)
          return ePluralType.One;
        if (GoogleLanguages.inRange(n % 100, 1, 10))
          return ePluralType.Few;
        return !GoogleLanguages.inRange(n % 100, 11, 19) ? ePluralType.Plural : ePluralType.Many;
      case 13:
        if (n % 10 == 1)
          return ePluralType.One;
        return n % 10 != 2 ? ePluralType.Plural : ePluralType.Two;
      case 14:
        return n % 10 != 1 || n % 100 == 11 ? ePluralType.Plural : ePluralType.One;
      case 15:
        if (n % 10 == 1 && n % 100 != 11 && n % 100 != 71 && n % 100 != 91)
          return ePluralType.One;
        if (n % 10 == 2 && n % 100 != 12 && n % 100 != 72 && n % 100 != 92)
          return ePluralType.Two;
        if ((n % 10 == 3 || n % 10 == 4 || n % 10 == 9) && n % 100 != 13 && n % 100 != 14 && n % 100 != 19 && n % 100 != 73 && n % 100 != 74 && n % 100 != 79 && n % 100 != 93 && n % 100 != 94 && n % 100 != 99)
          return ePluralType.Few;
        return n % 1000000 != 0 ? ePluralType.Plural : ePluralType.Many;
      case 16 /*0x10*/:
        switch (n)
        {
          case 0:
            return ePluralType.Zero;
          case 1:
            return ePluralType.One;
          case 2:
            return ePluralType.Two;
          case 3:
            return ePluralType.Few;
          case 6:
            return ePluralType.Many;
          default:
            return ePluralType.Plural;
        }
      default:
        return ePluralType.Plural;
    }
  }

  public static int GetPluralTestNumber(string langCode, ePluralType pluralType)
  {
