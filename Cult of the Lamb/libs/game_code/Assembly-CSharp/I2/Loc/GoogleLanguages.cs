  public static string GetLanguageName(
    string code,
    bool useParenthesesForRegion = false,
    bool allowDiscardRegion = true)
  {
    foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
    {
      if (code == keyValuePair.Value.Code)
      {
        string languageName = keyValuePair.Key;
        if (useParenthesesForRegion)
        {
          int length = languageName.IndexOf('/');
          if (length > 0)
            languageName = $"{languageName.Substring(0, length)} ({languageName.Substring(length + 1)})";
        }
        return languageName;
      }
    }
    if (allowDiscardRegion)
    {
      int length = code.IndexOf("-", StringComparison.Ordinal);
      if (length > 0)
        return GoogleLanguages.GetLanguageName(code.Substring(0, length), useParenthesesForRegion, false);
    }
    return (string) null;
  }

